Text::~Text() {
    if (labelBuffer) {
        delete[] labelBuffer->data;
        labelBuffer->data = nullptr;
    }
	delete labelBuffer;
    delete spriteBuffer;
}

void Text::ClearData(void) {
	SpaceInvaders::Sprite::ClearData();
}

void Text::UpdateSpriteBuffer(SpaceInvaders::Buffer* buffer) {
    drawTextBuffer(buffer, label);
}

void Text::SetText(const char* text) {
    if (label != text) {
        label = text;
    }
}

void Text::drawTextBuffer(SpaceInvaders::Buffer* buffer, const char* text) {
	stride = labelBuffer->size.width() * labelBuffer->size.height();
    int x = position.x();

	for (const char* charIndex = text; *charIndex != '\0'; ++charIndex) {
		char character = *charIndex - 32;
		if (character < 0 || character >= 65) {
			continue;
		}

        
        spriteBuffer->data = labelBuffer->data + (character * stride);

        drawSpriteBuffer(buffer, x, position.y());

        x += labelBuffer->size.width() + 1;
	}
}